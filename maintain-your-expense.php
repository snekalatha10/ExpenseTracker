                <h2> Here are the  Essential ways to manage your Expenses.</h2>
                <h2 class="section-heading">What are expenses?</h2>
                <p> "Expenses" are business costs that you pay for yourself or your family which the business may later reimburse you for.
                    <br> <blockquote class="blockquote">For Example, It would be like, if you use a
                    personal credit card to buy a train ticket to visit your client, or if you bought a
                    stamp at the post office and paid with your own cash. The name for this expense is
                    "out-of-pocket expenses".</blockquote>
                </p>

                <h2 class="section-heading">Record your expenses daily</h2>
                <p>The best time to record an expense is as soon as you spend the money - whenever possible, take a minute out of your day to record expenses on the go.
                    <br> <blockquote class="blockquote"> For example, if you're waiting on the
                    train platform, use that time to snap a photo of your train ticket and
                    upload the details of your expense to an online system like Expense Tracker.</blockquote>
                </p>

                <h2 class="section-heading">Review your expenses weekly</h2>
                <p>Track your frequent spending on a regular basis, which can give you an accurate picture
                    of where your money is going — and where you’d like it to go instead.
                    The best reason to <a href="home"> track your expense</a> is, that helps you stay focused on your finances.
                    When you regularly pay attention to your spending, you're more likely to stay within your
                    budget, and can even have extra savings each month.
                </p>

                <h2 class="section-heading">Keep expenses separate from other costs</h2>
                <p>When you're recording your expenses, it's essential to keep those expenses separate from costs that were paid for through your business's bank account.
                    <br> <blockquote class="blockquote">For example, if you pay for something on your business debit card rather than your personal credit card, remember that this cost would need to be recorded as paid by the business rather than by you.
                    Because the business paid for it directly and it is not your personal expense.</blockquote>
                </p>

                <h2 class="section-heading">Make sure your categories are consistent</h2>
                <p>It's harder to make calculations if you don't post your costs consistently into the same category of your accounts.
                    <br> <blockquote class="blockquote"> For example, if you put your car park ticket charge into "travel" one month and on "motor expenses" the next month, it's much harder to see whether your car parking costs are mounting up higher than you want them to be and it will make you assume that your expense for the car is increasing rather than you expect it to be.
                        </blockquote>
                    </p>
